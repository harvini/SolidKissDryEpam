public class Calculator {
		public int Addition(int i,int j) {
			return i+j;
		}
		public int Subtraction(int i,int j) {
			return i-j;
		}
		public int Multiplication(int i,int j) {
			return i*j;
		}
		public int Division(int i,int j) {
			return i/j;
		}
		public int Modulus(int i,int j) {
			return i%j;
		}
}